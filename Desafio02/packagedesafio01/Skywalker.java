package packagedesafio01;

public class Skywalker{
    private int skywalkerX, skywalkerY;

    public Skywalker(int x, int y){
        skywalkerX = x;
        skywalkerY = y;
    }

    public int getSkywalkerX() {
        return skywalkerX;
    }

    public int getSkywalkerY() {
        return skywalkerY;
    }
    
}
